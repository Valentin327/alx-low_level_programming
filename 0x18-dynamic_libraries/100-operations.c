#include <stdio.h>
/**
 * add - function that two numbers
 * @a: number to add to another
 * @b: number to add to another
 *
 * Return: sum of two number
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function that perform subtraction
 * of two number
 * @a: a number
 * @b: a number
 *
 * Return: result of difference
 * two number
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - function to divide
 * two number
 * @a: a number
 * @b: a number
 *
 * Return: result
 */
int div(int a, int b)
{
	return ((int)(a / b));
}
/**
 * mul - function to multiply
 * two numbers
 * @a: a number
 * @b: a number
 *
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - function to perform
 * modulo with two number
 * @a: a number
 * @b: a number
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
