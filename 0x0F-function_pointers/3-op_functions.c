#include "3-calc.h"

/**
 * op_add - function to perform addition of two numbers
 * @a: First number
 * @b: Second number
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to perform substraction of two numbers
 * @a: Fisrt number
 * @b: Second number
 * Return:difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to perform multiplication of two numbers
 * @a: First number
 * @b: Second number
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to perform division of two numbers
 * @a: First number
 * @b: Second number
 * Return: the result of the division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to perform modulo of two numbers
 * @a: First number
 * @b: Second number
 * Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
